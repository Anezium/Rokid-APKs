    private fun setupGestures() {
        gestureDetector = GestureDetector(this, object : GestureDetector.SimpleOnGestureListener() {
            override fun onDown(e: MotionEvent): Boolean = true

            override fun onSingleTapUp(e: MotionEvent): Boolean {
                onTapAction()
                return true
            }

            override fun onFling(
                e1: MotionEvent?,
                e2: MotionEvent,
                velocityX: Float,
                velocityY: Float,
            ): Boolean {
                if (e1 == null) return false
                val dx = e2.x - e1.x
                val dy = e2.y - e1.y
                val minDist = SWIPE_MIN_DISTANCE_DP * resources.displayMetrics.density
                if (Math.abs(dx) >= minDist && Math.abs(dx) > Math.abs(dy) * SWIPE_DOMINANCE) {
                    if (dx > 0) onSwipeRight() else onSwipeLeft()
                    return true
                }
                return false
            }
        })
    }

    override fun dispatchTouchEvent(event: MotionEvent): Boolean {
        gestureDetector.onTouchEvent(event)
        return true
    }

    override fun onKeyDown(keyCode: Int, event: KeyEvent?): Boolean {
        return when (keyCode) {
            KeyEvent.KEYCODE_DPAD_LEFT -> {
                onSwipeLeft(); true
            }
            KeyEvent.KEYCODE_DPAD_RIGHT -> {
                onSwipeRight(); true
            }
            KeyEvent.KEYCODE_DPAD_CENTER,
            KeyEvent.KEYCODE_ENTER,
            KeyEvent.KEYCODE_NUMPAD_ENTER,
            KeyEvent.KEYCODE_BUTTON_A,
            -> {
                onTapAction(); true
            }
            else -> super.onKeyDown(keyCode, event)
        }
    }

    private fun onSwipeLeft() {
        if (uiState != UiState.DEVICE_SELECT) return

        when (selectionZone) {
            SelectionZone.DEVICES -> {
                if (pairedDevices.size > 1 && selectedDeviceIndex > 0) {
                    shiftSelection(-1)
                }
            }

            SelectionZone.WIFI -> {
                selectionZone = SelectionZone.DEVICES
                updateDeviceActionUi()
            }
        }
    }

    private fun onSwipeRight() {
        if (uiState != UiState.DEVICE_SELECT) return

        when (selectionZone) {
            SelectionZone.DEVICES -> {
                val hasMoreDevices = pairedDevices.size > 1 && selectedDeviceIndex < pairedDevices.lastIndex
                if (hasMoreDevices) {
                    shiftSelection(1)
                } else {
                    selectionZone = SelectionZone.WIFI
                    updateDeviceActionUi()
                }
            }

            SelectionZone.WIFI -> {
                // End of the horizontal nav.
            }
        }
    }

    private fun onTapAction() {
        when (uiState) {
            UiState.DEVICE_SELECT -> {
                when (selectionZone) {
                    SelectionZone.DEVICES -> ensurePermissionsThenStart(manual = true)
                    SelectionZone.WIFI -> openWifiSettings()
                }
            }
            UiState.RESULT -> {
                transitionTo(UiState.DEVICE_SELECT)
                refreshPairedDevices()
            }
            else -> { /* ignore during transfer */ }
        }
    }

